    public void carregaTabela(){
        
            DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();
            modelo.setNumRows(0);
            
            ProdutoDAO produto = new ProdutoDAO();
            List<Produto> lista = produto.listarCarrinho();
            for(Produto p: lista){
                modelo.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getValor(),
                    p.getDescricao(),
                    p.getCategoria(),
                    p.getQuantidade()
            });
        }
    }

   
